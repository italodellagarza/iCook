class Receita {
  String nomeReceita;
  String modoDeFazer;
  String rendimento;
  String ingredientes;

  Receita(nomeReceita, ingredientes, modoDeFazer, rendimento) {
    this.nomeReceita = nomeReceita;
    this.ingredientes = ingredientes;
    this.modoDeFazer = modoDeFazer;
    this.rendimento = rendimento;
  }
}
